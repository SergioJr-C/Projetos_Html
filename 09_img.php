<h2>Brincadeiras à Parte</h2>

<p>Segundo Pedro Francisco Borges Pereira, diretor técnico da PPAI Sistemas
É triste, e me envergonha admitir, mas é verdade: para muitos profissionais de TI, dizer que “o usuário é burro” é um chavão! 
Pois bem, na minha opinião, esta maneira de pensar – este paradigma – leva a um desempenho extremamente pobre: help desks que não resolvem problemas, 
sistemas que não atendem os requisitos, etc.  Em resumo, este paradigma incorreto leva a ferramentas de informática 
que não contribuem para os objetivos estratégicos da organização!
O usuário pode não entender de configurações de rede, nem de especificação de requisitos.
Mas é ele que faz a empresa funcionar – e é ele que traz o lucro que torna as empresas viáveis!
Se a empresa em que você trabalha produz celulose, 
o seu objetivo principal como profissional de TI não é “administrar a rede”, 
nem “desenvolver software”.  A sua tarefa é “ajudar a produzir celulose”.  
Ou equipamentos para poços de petróleo, ou carros, ou alimentos!  Sim, 
você vai utilizar as suas habilidades e capacitações específicas, técnicas.  
Mas a sua missão não é “escrever código genial”.  
É contribuir para a concretização dos objetivos da empresa!</p>

